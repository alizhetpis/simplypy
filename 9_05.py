# Вот пример функции, которая принимает на вход две функции f и g и возвращает новую функцию h, которая является композицией f и g:

def compose(f, g):
    """
    Функция для создания композиции двух функций
    """
    def h(x):
        return f(g(x))
    return h


# Функция принимает два аргумента: функции f и g. В теле функции создается новая функция h, 
# которая принимает один аргумент x и возвращает результат вызова функции f с аргументом, являющимся результатом вызова функции g с тем же аргументом x.

# Пример использования функции:

def square(x):
    return x ** 2

def add_one(x):
    return x + 1

# Создание новой функции, которая является композицией функций square и add_one
composed_function = compose(square, add_one)

# Вызов новой функции с аргументом 2
result = composed_function(2)

print(result) # 9

# В этом примере мы создали две функции square и add_one, а затем создали новую функцию composed_function, которая является композицией этих двух функций. 
# Когда мы вызываем composed_function(2), она сначала вызывает функцию add_one(2), которая возвращает 3, а затем вызывает функцию square(3), которая возвращает 9.