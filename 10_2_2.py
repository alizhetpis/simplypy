# Вот пример программы, которая использует встроенные функции any() и all() для проверки списка на наличие элементов, 
# удовлетворяющих заданному условию, и на то, удовлетворяют ли все элементы списка этому условию:

numbers = [1, 2, 3, 4, 5]

# Проверяем, есть ли в списке хотя бы один элемент, который больше 3
if any(num > 3 for num in numbers):
    print("В списке есть хотя бы одно число больше 3")

# Проверяем, удовлетворяют ли все элементы списка условию: числа должны быть меньше 10
if all(num < 10 for num in numbers):
    print("Все числа в списке меньше 10")
else:
    print("Не все числа в списке меньше 10")


